package comment;

public class Comment {
	private int commentID;
	private String userID;
	private String tradeComment;
	private int commentAvailable;
	private int tradeID;
	private String commentDate;
	
	public int getTradeID() {
		return tradeID;
	}
	public void setTradeID(int tradeID) {
		this.tradeID = tradeID;
	}
	public String getCommentDate() {
		return commentDate;
	}
	public void setCommentDate(String commentDate) {
		this.commentDate = commentDate;
	}
	public int getCommentID() {
		return commentID;
	}
	public void setCommentID(int commentID) {
		this.commentID = commentID;
	}
	public int getCommentAvailable() {
		return commentAvailable;
	}
	public void setCommentAvailable(int commentAvailable) {
		this.commentAvailable = commentAvailable;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getTradeComment() {
		return tradeComment;
	}
	public void setTradeComment(String tradeComment) {
		this.tradeComment = tradeComment;
	}
	
}
